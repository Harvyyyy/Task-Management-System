<?php
require 'db.php';

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $title = $_POST['title'];
    $description = $_POST['description'];
    $due_date = $_POST['due_date'];
    $priority = $_POST['priority'];

    $stmt = $conn->prepare("INSERT INTO tasks (title, description, due_date, priority) VALUES (:title, :description, :due_date, :priority)");
    $stmt->execute([
        ':title' => $title,
        ':description' => $description,
        ':due_date' => $due_date,
        ':priority' => $priority,
    ]);

    header('Location: index.php');
    exit;
}
